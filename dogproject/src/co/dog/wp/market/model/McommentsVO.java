package co.dog.wp.market.model;

public class McommentsVO {
	String id;
	String regdt;
	String seq;
	String mcomments;
	String c_seq;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRegdt() {
		return regdt;
	}
	public void setRegdt(String regdt) {
		this.regdt = regdt;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getMcomments() {
		return mcomments;
	}
	public void setMcomments(String mcomments) {
		this.mcomments = mcomments;
	}
	public String getC_seq() {
		return c_seq;
	}
	public void setC_seq(String c_seq) {
		this.c_seq = c_seq;
	}
	
}